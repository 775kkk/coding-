package VYZ.ErmakovJava.ekzamenzima.datamigrate.parser.exception;

public class IllegalStringToParseException extends ParseException {
    public IllegalStringToParseException() {
        super();
    }
    public IllegalStringToParseException(String s) {
        super(s);
    }
    public IllegalStringToParseException(String s, Throwable cause) {
        super(s, cause);
    }
    public IllegalStringToParseException(Throwable cause) {
        super(cause);
    }
}
