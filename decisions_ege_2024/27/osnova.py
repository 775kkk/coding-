n=6
a=[100,8,33,45,19,90]
mc1=mc2=mn1=mn2=0
for i in range(n):
    if a[i]%2==0:
        if a[i]>mc1:#типа реализация перетекания...
            mc2=mc1
            mc1=a[i]
        elif a[i]>mc2:
            mc2=a[i]
    else:
        if a[i]>mn1:
            mn2=mn1
            mn1=a[i]
        elif a[i]>mn2:
            mn2=a[i]
print(mc1+mc2)#190 - ответ, тк он >78 (макс среди этих двоих)
print(mn1+mn2)#78
